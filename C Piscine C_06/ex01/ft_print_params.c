#include <unistd.h>

int	main(int argv, char **argc)
{
	int	i;
	int	c;

	if (argv > 1)
	{
		c = 1;
		i = 0;
		while (c < argv)
		{
			while (argc[c][i] != '\0')
			{
				write(1, &argc[c][i], 1);
				i++;
			}
			write(1, "\n", 1);
			i = 0;
			c++;
		}
	}
	return (0);
}
