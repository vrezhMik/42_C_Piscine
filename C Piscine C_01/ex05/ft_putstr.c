#include <stdio.h>
#include <unistd.h>

void	ft_putstr(char *str);

void	ft_putchar(char c);

void	ft_put_char(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		ft_put_char(*str);
		str++;
	}
}
